#include <iostream>
using namespace std;
int main(){
    //binary operators
    //Addition
    cout<<(10+9)<<endl;
    //Subration
    cout<<(100-90)<<endl;
    //multiplication
    cout<<(10*5)<<endl;
    //division
    cout<<(10/5)<<endl;
    //modulo
    cout<<(500%10)<<endl;
    // OR
    int a;
    int b;
    cout<<"Enter the first number = ";
    cin>>a;
    cout<<"Enter the second number = ";
    cin>>b;
    cout<<"Addition :- "<<(a+b)<<endl;
    cout<<"Subraction :- "<<(a-b)<<endl;
    cout<<"Multiplication :- "<<(a*b)<<endl;
    cout<<"Division :- "<<(a/b)<<endl;
    cout<<"Modulo :- "<<(a%b)<<endl;
    return 0;
}